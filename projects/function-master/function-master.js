// Function 10 - Has Words ///////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

function hasWord(string, word) {
var wordsArray = string.split(' ');
for (var i = 0; i < wordsArray.length; i++) {
    if (wordsArray[i] === word) {
        return true
    }
}
return false
}

