function drawChessboard(x) {
  let board = [];
  for (let i = 0; i < x; i++) {
    let row = '';
    if (i % 2 === 0) {
      row = ' '
      for (let j = 1; j < x; j++) {
        if (j % 2 === 0) {
          row += ' '
        } else { 
          row += '#'
        }
      }
      
    } else {
      row = '#' 
      for (let j = 1; j < x; j++) {
        if (j % 2 === 0) { 
          row += '#'
        } else { 
          row += ' '  // ''
        }
        
    }
  } 
 board.push(row)
}
console.log(board.join('\n') + '\n')
}

